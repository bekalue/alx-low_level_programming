/**
 * add - adds two passed integer values.
 * @x: an integer.
 * @y: an integer.
 *
 * Return: the sum.
 */
int add(int x, int y)
{
	return (x + y);
}
/**
 * sub - substracts two passed integer values.
 * @x: an integer.
 * @y: an integer.
 *
 * Return: the diff
 */
int sub(int x, int y)
{
	return (x - y);
}
/**
 * mul - multiplies two passed integer values.
 * @x: an integer
 * @y: an integer
 *
 * Return: result
 */
int mul(int x, int y)
{
	return (x * y);
}
/**
 * div - divides the two passed integer values
 * @x: an integer
 * @y: an integer
 *
 * Return: the result
 */
int div(int x, int y)
{
	return (x / y);
}
/**
 * mod - computes modulo
 * @x: an integer
 * @y: an integer
 *
 * Return: the modulus
 */
int mod(int x, int y)
{
	return (x % y);
}
